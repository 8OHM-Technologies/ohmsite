    <p>Due to the digital, downloadable, and instantly accessible nature of our once-off datasets, and in terms of
        Section 42(2)(f)(iii) of ECTA, <strong>all change-of-mind sales are final and strictly non-refundable</strong>
        once access or download links have been generated.</p>

    <h3>Defective Data &amp; Statutory Warranties (CPA Section 56)</h3>
    <p>In accordance with the statutory Implied Warranty of Quality, if a dataset is defective, corrupted, or severely
        misrepresented, you are legally entitled to choose between a **full refund** or a **functional replacement
        file**. This applies under the following verified circumstances:</p>
    <ul>
        <li><strong>Technical Corruption:</strong> The delivered file is corrupted, unreadable, or structurally
            defective. You may elect an immediate replacement or a full refund.</li>
        <li><strong>Severe Misrepresentation:</strong> The dataset delivered is fundamentally different from what was
            advertised or described in our official product documentation. <em>Please note: General dissatisfaction with
                market insights or specific data points does not qualify as misrepresentation.</em></li>
        <li><strong>Accidental Non-Delivery:</strong> You paid for the dataset but never received the download link or
            API access, and you notify us within 7 days of purchase.</li>
    </ul>

