    <h2 id="1-subscription-based-saas-product">1. Subscription-Based SaaS Product</h2>
    <p>We offer monthly and annual subscription plans for our SaaS platform.</p>

    <h3>Cancellation Policy</h3>
    <ul>
        <li><strong>Cancel Anytime:</strong> You can cancel your subscription at any time through your account dashboard
            or by contacting us via the contact form on www.8ohm.co.za.</li>
        <li><strong>Access Until End of Period:</strong> For standard monthly cancellations, your subscription will
            remain active, and you will retain full access to the SaaS platform until the end of your current billing
            cycle.</li>
        <li><strong>No Auto-Renewal:</strong> Cancelling your subscription ensures you will not be charged for the next
            billing cycle.</li>
    </ul>

    <h3>Refund Policy</h3>
    <ul>
        <li><strong>Monthly Subscriptions:</strong> To keep our pricing fair and sustainable, we do not offer partial
            refunds or prorated credits for mid-cycle cancellations on rolling month-to-month plans.</li>
        <li><strong>Annual Subscriptions (CPA Section 14):</strong> In terms of the South African Consumer Protection
            Act, subscribers who are natural persons (individuals) are entitled to cancel fixed-term annual plans
            mid-term by giving us <strong>20 business days' written notice</strong>. Upon processing, a prorated refund
            will be issued, subject to a reasonable cancellation penalty to cover direct administrative and onboarding
            costs. <em>Please note: This statutory right does not apply to juristic persons (corporate accounts, trusts,
                or businesses exceeding the CPA regulatory threshold).</em></li>
        <li><strong>Money-Back Guarantee:</strong> We offer a 7-day money-back guarantee for first-time subscribers. If
            you are unhappy with the platform within your first 7 days, please contact us for a full refund. This
            applies only to your initial purchase and does not apply to renewals.</li>
    </ul>

